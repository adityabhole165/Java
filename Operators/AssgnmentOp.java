package Operators;

public class AssgnmentOp {
    public static void main(String[] args) {
        int a= 10;
        int b = 5;
        a += 10;
        System.out.println(a);
        b-=1;
        System.out.println(b);
        a*=6;
        System.out.println(a);
        b/=2;
        System.out.println(b);
        a%=3;
        System.out.println(a);
    }
}
