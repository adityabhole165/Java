package javaBasics;

public class Typecasting {
    public static void main(String[] args) {
        float a = 25.12f;
        int b = (int)a;
        System.out.println(b);

        char ch = 'a';
        char ch2 = 'b';

        int Number = ch;
        int Number2 = ch2;

        System.out.println(Number);
        System.out.println(Number2);
    }
}
